public class Cubo {
    private double alto;
    private double largo;
    private double ancho;

    public Cubo(double alto, double largo, double ancho) {
        this.alto = alto;
        this.largo = largo;
        this.ancho = ancho;
    }

    public double getAlto() {
        return alto;
    }

    public void setAlto(double alto) {
        this.alto = alto;
    }

    public double getLargo() {
        return largo;
    }

    public void setLargo(double largo) {
        this.largo = largo;
    }

    public double getAncho() {
        return ancho;
    }

    public void setAncho(double ancho) {
        this.ancho = ancho;
    }
    
    
}
